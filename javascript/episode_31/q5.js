/*

Der "Strict Mode" ist eine Funktion in JavaScript, 
die eingeführt wurde, um das Verhalten der Sprache 
restriktiver zu gestalten und potenzielle Fehlerquellen 
sowie unsichere Praktiken zu reduzieren. 

Die Hauptaufgaben des Strict Modes sind:

- Vermeidung von fehlerhaften oder unsicheren Praktiken
- Bessere Fehlererkennung
- Verbesserung der Leistungsoptimierung
- Vorbereitung für zukünftige ECMAScript-Versionen

Es ist eine bewährte Praxis, den Strict Mode zu verwenden, 
um den Code robuster zu gestalten, die Fehlersuche zu erleichtern 
und sicherzustellen, dass der Code für zukünftige Entwicklungen bereit ist.

PS: In einem ES6 Modul brauchen wir kein 'use strict'!
*/
