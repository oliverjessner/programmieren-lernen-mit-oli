/* Q7: Was ist der Unterschied zwischen "null" und "undefined" in JavaScript? */
/*
"undefined":

Eine Variable, die deklariert wurde, aber derzeit keinen Wert hat, 
hat den Wert "undefined". Eine Funktion, die keine explizite 
Rückgabeanweisung hat, gibt implizit "undefined" zurück.
Ein Objekt-Eigenschaftszugriff, bei dem die Eigenschaft nicht vorhanden 
ist, liefert ebenfalls "undefined".
Beim Deklarieren einer Variablen ohne Wertzuweisung wird sie standardmäßig 
mit "undefined" initialisiert.

"null":

"null" ist ein Wert, der anzeigt, dass eine Variable bewusst auf 
einen leeren oder nicht vorhandenen Wert gesetzt wurde.
Es wird oft verwendet, um anzuzeigen, dass eine Variable absichtlich 
keinen Wert hat. Im Gegensatz zu "undefined" ist "null" ein expliziter 
Wert, der von dir zugewiesen werden kann.

Zusammenfassend gesagt, zeigt "undefined" an, dass etwas noch nicht definiert 
oder initialisiert wurde, während "null" bewusst verwendet wird, 
um auszudrücken, dass eine Variable keinen Wert hat.
*/
